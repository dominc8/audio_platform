function [y] = fft_16p(x)
    w = exp(-j*pi/8);
    w2 = w^2;
    w3 = w^3;
    a = fft_4p(x(1:4:13)); 
    b = fft_4p(x(3:4:15)) .* [1, w2, -j, -j*w2]; 
    c = fft_4p(x(2:4:14)) .* [1, w, w2, w3];
    d = fft_4p(x(4:4:16)) .* [1, w3, -j*w2, -w];
    
    s = fft_4p([a(1), c(1), b(1), d(1)]);
    t = fft_4p([a(2), c(2), b(2), d(2)]);
    u = fft_4p([a(3), c(3), b(3), d(3)]);
    v = fft_4p([a(4), c(4), b(4), d(4)]);
    
    y(1:4:13) = s;
    y(2:4:14) = t;
    y(3:4:15) = u;
    y(4:4:16) = v;
end

